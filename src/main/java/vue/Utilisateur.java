    public void remplir(JTable table){
        List<modele.Utilisateur> list = utilisateurController.afficherUtilisateur();
        DefaultTableModel model = (DefaultTableModel)table.getModel();
        model.setRowCount(0);
        Object[]row;
        for(modele.Utilisateur utilisateur:list){
            row = new Object[4];
            row[0] = utilisateur.getId();
            row[1] = utilisateur.getNom();
            row[2] = utilisateur.getMail();
            row[3] = utilisateur.getTelephone();

            model.addRow(row);
        }
    }
    
