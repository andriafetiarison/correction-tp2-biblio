    private void jButton2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton2MouseClicked
        if(jButton2.getText() == "Ajouter"){
            String nomValue = nom.getText();
            String mailValue = mail.getText();
            String telephoneValue = telephone.getText();

            if(nomValue.isEmpty() || mailValue.isEmpty() || telephoneValue.isEmpty()){
                JOptionPane.showMessageDialog(null,"Veuillez compléter le champ","Attention",JOptionPane.WARNING_MESSAGE);
            }
            else{
                modele.Utilisateur utilisateur = new modele.Utilisateur();
                utilisateur.setNom(nomValue);
                utilisateur.setMail(mailValue);
                utilisateur.setTelephone(telephoneValue);
                
                if(utilisateur.verifierMail(mailValue) == true){
                    if(utilisateur.verifierTelephone(telephoneValue) == true){
                        utilisateurController.ajouterUtilisateur(utilisateur);

                        JOptionPane.showMessageDialog(null,"Ajout avec succès!","Information",JOptionPane.INFORMATION_MESSAGE);
                        initialise();
                        remplir(table);
                    }
                    else{
                        JOptionPane.showMessageDialog(null,"Numéro de téléphone invalide","Attention",JOptionPane.WARNING_MESSAGE);
                    }
                }
                else{
                   JOptionPane.showMessageDialog(null,"Adresse email invalide","Attention",JOptionPane.WARNING_MESSAGE); 
                }
            }
        }
        else{
            String nomValue = nom.getText();
            String mailValue = mail.getText();
            String telephoneValue = telephone.getText();
            String numeroValue = numero.getText();
            int num = Integer.parseInt(numeroValue);

            if(nomValue.isEmpty() || mailValue.isEmpty() || telephoneValue.isEmpty()){
                JOptionPane.showMessageDialog(null,"Veuillez compléter le champ","Attention",JOptionPane.WARNING_MESSAGE);
            }
            else{
                modele.Utilisateur utilisateur = new modele.Utilisateur();
                utilisateur.setNom(nomValue);
                utilisateur.setMail(mailValue);
                utilisateur.setTelephone(telephoneValue);
                utilisateur.setId(num);
                
                if(utilisateur.verifierMail(mailValue) == true){
                    if(utilisateur.verifierTelephone(telephoneValue) == true){
                        utilisateurController.modifierUtilisateur(utilisateur);
                
                        JOptionPane.showMessageDialog(null,"Modification avec succès!","Information",JOptionPane.INFORMATION_MESSAGE);
                        initialise();

                        jLabel6.setText("Ajout d'un nouveau utilisateur");
                        jButton2.setText("Ajouter");
                        remplir(table);
                    }
                    else{
                        JOptionPane.showMessageDialog(null,"Numéro de téléphone invalide","Attention",JOptionPane.WARNING_MESSAGE);
                    }
                }
                else{
                   JOptionPane.showMessageDialog(null,"Adresse email invalide","Attention",JOptionPane.WARNING_MESSAGE); 
                }
            }
        }
    }//GEN-LAST:event_jButton2MouseClicked

    private void jToggleButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jToggleButton1MouseClicked
        int itemSelected = table.getSelectedRow();
        if(table.isRowSelected(itemSelected)){
            String numeroValue = (String.valueOf(table.getValueAt(table.getSelectedRow(),0)));
            int num = Integer.parseInt(numeroValue);

            int reponse = JOptionPane.showConfirmDialog(this,"Voulez-vous vraiment supprimer cet utilisateur ?",  "Confirmation",JOptionPane.YES_NO_OPTION);
            if(reponse == JOptionPane.YES_OPTION){
                utilisateurController.supprimerUtilisateur(num);
                remplir(table);
                JOptionPane.showMessageDialog(null,"Suppression bien réussie!","Information",JOptionPane.INFORMATION_MESSAGE);
            }
        }else{
            JOptionPane.showMessageDialog(null,"Veuillez d'abord sélectionnez l'élément à supprimer","Attention",JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_jToggleButton1MouseClicked

    private void jButton3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton3MouseClicked
        int itemSelected = table.getSelectedRow();
        if(table.isRowSelected(itemSelected)){
            numero.setText(String.valueOf(table.getValueAt(table.getSelectedRow(),0)));
            nom.setText(String.valueOf(table.getValueAt(table.getSelectedRow(),1)));
            mail.setText(String.valueOf(table.getValueAt(table.getSelectedRow(),2)));
            telephone.setText("0" + String.valueOf(table.getValueAt(table.getSelectedRow(),3)));

            jLabel6.setText("Edition d'un utilisateur");
            jButton2.setText("Modifier");
        }else{
            JOptionPane.showMessageDialog(null,"Veuillez d'abord sélectionnez l'élément à modifier","Attention",JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_jButton3MouseClicked


